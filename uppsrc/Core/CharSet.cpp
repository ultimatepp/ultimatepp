void CharSetData::Gen()
{
	int c;
	line[0] = new byte[128];
	for(c = 0; c < 128; c++)
		line[0][c] = c;
	for(c = 0; c < 128; c++) {
		int q = table[c];
		if(q >= 128) {
			if(q < 16 * 128) {
				int i = q >> 7;
				if(!line[i]) {
					line[i] = new byte[128];
					memset(line[i], CUNDEF, 128);
				}
				line[i][q & 0x7f] = c + 128;
			}
			else
			if((q & 0xFF00) != 0xEE00)
				map.Add(q, c + 128);
		}
	}
	for(c = 0; c < 16; c++)
		if(!line[c])
			line[c] = undef;
}

inline int CharSetData::FromUnicode(int chr, int def)
{
	if(!line[0]) Gen();
	int c = chr < 16 * 128 ? line[chr >> 7][chr & 0x7f]
	                       : (chr & 0xFF00) == 0xEE00 ? (chr & 255)
	                                                  : map.Get(chr, CUNDEF);
	return c == DEFAULTCHAR ? def : c;
}

inline void CharSetData::FromUnicode(char *t, const wchar *s, const wchar *end, int def)
{
	if(!line[0]) Gen();
	if(def == DEFAULTCHAR)
		while(s < end) {
			int chr = *s++;
			*t++ = chr < 16 * 128 ? line[chr >> 7][chr & 0x7f]
			                      : (chr & 0xFF00) == 0xEE00 ? (chr & 255)
			                                                 : map.Get(chr, CUNDEF);
		}
	else
		while(s < end) {
			int chr = *s++;
			int c = chr < 16 * 128 ? line[chr >> 7][chr & 0x7f]
			                       : (chr & 0xFF00) == 0xEE00 ? (chr & 255)
			                                                  : map.Get(chr, CUNDEF);
			*t++ = c == DEFAULTCHAR ? def : c;
		}
}

CharSetData::CharSetData()
{
	for(int i = 0; i < 16; i++)
		line[i] = 0;
}

CharSetData::~CharSetData()
{
	for(int i = 0; i < 16; i++)
		if(line[i] && line[i] != undef)
			delete[] line[i];
}

